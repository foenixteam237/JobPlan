@Composable
fun TopBarSection(
    userName: String,
    profile: Painter,
    isOline: Boolean) {
    Card( modifier = Modifier
        .fillMaxWidth()
        .height(60.dp)
        .background(Color(0XFFFAFAFA)),
        // elevation = 4.dp
    ) {

        Row(
            modifier = Modifier
                .fillMaxSize()
                .padding(horizontal = 8.dp),
            verticalAlignment = Alignment.CenterVertically
        ) {
            Image(
                painter = profile,
                contentDescription = null,
                modifier = Modifier
                    .size(40.dp)
                    .clip(CircleShape),
                contentScale = ContentScale.Crop
            )
            Spacer(modifier = Modifier.width(8.dp))
            Column {
                Text(text = userName, style = TextStyle(
                    fontSize = 12.sp,
                    fontWeight = FontWeight.Bold
                )
                )
                Text(text = if (isOline) "Online" else "Offline", fontWeight = FontWeight.SemiBold)

            }
        }
    }
}
